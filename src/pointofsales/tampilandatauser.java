    private Object[] pilihdata() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String id = jTable1.getValueAt(selectedRow, 0).toString();
            String nama = jTable1.getValueAt(selectedRow, 1).toString();
            String username = jTable1.getValueAt(selectedRow, 2).toString();
            String password = jTable1.getValueAt(selectedRow, 3).toString();
            String level = jTable1.getValueAt(selectedRow, 4).toString();
            return new Object[]{id, nama, username, password, level};
        }
        return null;
    }
    
    
    private void setupSearch() {
        jTextField1.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void changedUpdate(DocumentEvent e) {
                search();
            }
            @Override
            public void removeUpdate(DocumentEvent e) {
                search();
            }
            @Override
            public void insertUpdate(DocumentEvent e) {
                search();
            }
        });
    }

    private void search() {
        String query = jTextField1.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>((DefaultTableModel) jTable1.getModel());
        jTable1.setRowSorter(sorter);

        if (query.length() == 0) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query));
        }
    }

    void viewDataUser() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data

        // Membuat renderer untuk menampilkan data di tengah
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(javax.swing.JLabel.CENTER);
        try (Connection conn = config.configDB()) {
            String sql = "SELECT id_akun, nama, username, password, level FROM akun";
            try (PreparedStatement pst = conn.prepareStatement(sql);
                 ResultSet rs = pst.executeQuery()) {

                while (rs.next()) {
                    Object[] row = {
                    rs.getString("id_akun"),
                    rs.getString("nama"),
                    rs.getString("username"),
                    rs.getString("password"),
                    rs.getString("level"),
                };
                    model.addRow(row);
                }

                // Terapkan centerRenderer ke setiap kolom
                for (int i = 0; i < jTable1.getColumnModel().getColumnCount(); i++) {
                    jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }


